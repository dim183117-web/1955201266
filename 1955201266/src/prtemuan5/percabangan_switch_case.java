public class percabangan_switch_case {
    public static void main(String[] args) {
        int nomor = 4;
        switch (nomor)
        {
            case 1:
                System.out.println("makan");
                break;
            case 2:
                System.out.println("minum");
                break;
            case 3:
                System.out.println("tisu");
                break;
            case 4:
                System.out.println("camilan");
                break;
            case 5:
                System.out.println("selesai");
                break;
        }
    }
    
}

    

